-- Zapytanie:
-- Ponieważ w treści jest powiedziane "DLA KAŻDEGO pracownika o ryzyku 1 [...]
-- należy wypisać wszystkich pracowników [...]", poniższe zapytanie wypisuje NULL
-- przy pracownikach, którzy nie opublikowali żadnej pracy zbiorowej, ponieważ
-- pomimo tego spełniają warunki ze zdania składowego z kwantyfikatorem. 
SELECT a.id AS pracownik, b.id AS wspolautor
FROM autor a LEFT JOIN autor b ON (
	b.id IN (
		SELECT autor FROM autorstwo WHERE praca IN (
			SELECT praca FROM autorstwo WHERE autor = a.id
		)
	)
	AND a.id <> b.id
)
WHERE a.ryzyko = 1 AND 0 = (
	SELECT COUNT(*) FROM praca p WHERE a.id IN (
		SELECT autor FROM autorstwo WHERE praca = p.id
	)
	AND p.rok = 2020
);
